//relation operator
#include<stdio.h>

int main(){
    int num1 = 30;
    int num2 = 55;

    printf("< : num1 < num2 = %d\n", num1 < num2 );
    printf("> : num1 < num2 = %d\n", num1 > num2 );
    printf("<= : num1 < num2 = %d\n", num1 <= num2 );
    printf(">=: num1 < num2 = %d\n", num1 >= num2 );
    printf("== : num1 < num2 = %d\n", num1 == num2 );
    printf("!= : num1 < num2 = %d\n", num1 != num2 );

    return 0 ;
}